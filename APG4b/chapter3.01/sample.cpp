#include <bits/stdc++.h>
using namespace std;
int main() {
  int64_t a = 2000000000;
  int64_t b = a * 2;
  cout << b << endl;
  cout << "----------------" << endl;


  cout << 3.14159265358979 << endl;
 
  // 小数点以下10桁まで
  cout << fixed << setprecision(10);
 
  cout << 3.14159265358979 << endl;
  cout << "------------------" << endl;


  cout << fixed << setprecision(5);

  int c = 5;
  cout << (double)c << endl;

  double d = 3.141592;
  cout << (int)d << endl;

}
