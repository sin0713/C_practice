#include <bits/stdc++.h>
using namespace std;

int main()
{
  int name;
  name = 10;
  cout << name << endl;

  name = 5;
  cout << name << endl;
  cout << "----------------" << endl;

  int a;
  cin >> a;
  cout << a * 10 << endl;
  cout << "----------------" << endl;

  int b, c, d;
  cin >> b >> c >> d;
  cout << b * c * d << endl;
}
